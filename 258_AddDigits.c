/* Analysis
(1) Digital Root:
12345 = 1 × (9,999 + 1) + 2 × (999 + 1) + 3 × (99 + 1) + 4 × (9 + 1) + 5.
       = (1 × 9,999 + 2 × 999 + 3 × 99 + 4 × 9) + (1 + 2 + 3 + 4 + 5).
==> dr(12345) = dr(1 + 2 + 3 + 4 + 5) = dr(15)

15 = 1 * (9 + 1) + 5 
   = 1 * 9 + 1 + 5
==> dr(15) = dr(1 + 5) = dr(6)
*/

int addDigits(int num) {
     return (num - 1) % 9 + 1;
}
